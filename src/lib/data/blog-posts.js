// Helper function to get all posts
export function getAllPosts() {
	return mockBlogPosts;
}

// Helper function to get post by slug
export function getPostBySlug(slug) {
	return mockBlogPosts.find(post => post.slug === slug);
}

// Helper function to get featured posts
export function getFeaturedPosts() {
	return mockBlogPosts.filter(post => post.featured);
}

// Helper function to get posts by category
export function getPostsByCategory(categorySlug) {
	return mockBlogPosts.filter(post => post.category.slug === categorySlug);
}

// Helper function to search posts
export function searchPosts(query) {
	const searchTerm = query.toLowerCase();
	return mockBlogPosts.filter(post => 
		post.title.toLowerCase().includes(searchTerm) ||
		post.excerpt.toLowerCase().includes(searchTerm) ||
		post.tags.some(tag => tag.toLowerCase().includes(searchTerm)) ||
		post.author.name.toLowerCase().includes(searchTerm)
	);
}

// Helper function to get related posts
export function getRelatedPosts(currentPostId, categoryId, limit = 3) {
	return mockBlogPosts
		.filter(post => post.id !== currentPostId && post.category.id === categoryId)
		.slice(0, limit);
}
